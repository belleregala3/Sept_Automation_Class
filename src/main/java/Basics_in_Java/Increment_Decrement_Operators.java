package Basics_in_Java;

public class Increment_Decrement_Operators {

	public static void main(String[] args) {
		
		//   ++			means +1
		//   --			means -1
		
		//post increment operator
		//pre increment operator
		
		//post decrement operator
		//pre decrement operator
		
		int a = 1;
		
		a++;	//this is post increment operator, after the variable ++ is used
		++a; 	//this is pre increment operator, before the variable -- is used

		a--;	//this is post decrement operator, after the variable -- is used
		--a;	//this is pre decrement operator, before the variable -- is used
		
		
		int i = 1;
		i++;
		System.out.println(i);
	}

}
